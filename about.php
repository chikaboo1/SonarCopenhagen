<h2 class="border-top">Sonar</h2>

<img class="sonar-logos" src="images/pages/aboutus.png" alt="sonar logo image">

<p>Created in 1994, Sónar is a pioneering festival with a unique format and content. It's first class reputation as a leading reference for international festivals is by virtue of a carefully balanced cultural offering, combining a playful nature, the avant-garde, and experimentation with electronic dance music's newest trends.</p>

<p>Sónar is constantly on the pulse of the current electronic music landscape. Its interactions and hybridizations with digital creation and new media, unites established artists with emerging talent, in all areas of musical and audiovisual production.</p>

<p>Sónar's founding values have defined the festival since its inception: the link between creativity and technology, its commitment to being a global event and a meeting point for creative people from both public and industry sectors, from different disciplines and communities.
Sónar Barcelona's activities are divided into two main locations: the recently premiered Sónar by Day, located from 2013 in Fira Montjuïc, with concerts and dj's, showcases as well as the new Sónar+D area, the essence of which is discovering and tracking new talent and enhance professional activities; and the larger shows at Sónar by Night, in Fira Gran Via de L'Hospitalet, where the leading names in the current international music scene are staged.</p>

<p>Sónar+D presents a program with a common goal: to make the relationship between creativity, technology, mobility, innovation and business more transparent and accessible. The birth of Sónar+D in 2013 expresses a concrete commitment to Sónar's professional activity, linking creative and technology companies of all kinds.</p>

<p>Since 2002 Sónar has organized more than 50 events in many different parts of the world, adapting the Sónar philosophy to unique venues and environments, and highlighting the most interesting homegrown talent and the scene in the city where it takes place. As well as the event in Barcelona, Sónar organizes festivals every year in other cities. In previous years, it has travelled to Reykjavik, Buenos Aires, New York, London, Frankfurt, Seoul, Lisboa, Lyon, Hamburg, Toronto, Montreal, Chicago, Boston, Denver, Oakland, Los Ángeles, Tokyo and Osaka, among many other destinations around the world.</p>

